◊sc{Common Lisp} is … by far the best programming environment available to
current-day programmers.}

Saying something over and over again doesn't make it any more true.

It's not helpful, when you want to show people the interesting parts of Lisp,
to call it the ◊scare{ultimate language}. Like anything, Lisp has upsides and
downsides. If you're someone who likes Lisp, (I hope) it's not because people
have ◊emph{told} you it's great – rather, it's because you've spent time with
it, you've created things with it, and you've decided that its pros outweigh
its cons◊aside{Or that ◊function{cons} is its best pro!}.

This is perhaps a more general point about making good arguments: that you
should give people the facts so they come to their own conclusion. Stuffing
your prose with zealous rhetoric doesn't make the facts more factual – it just
hides the real, honest truth from the reader.

This method of persuasion is called ◊first-use{logos}. It is one of
◊person{Aristotle}'s ◊first-use{three modes of persuasion}◊aside{
Though it turns out that there is a fourth, ◊first-use{kairos}, which is where
the orator uses the time and the setting to their advantage – however, this
isn't key to ◊emph{written} arguments.},
and Aristotle describes it not just as being the logic behind an argument, but
also as giving a satisfaction to the listeners, who are given the freedom to
make their own conclusions.
